import turtle

# Je veux que ma tortue dessine un pentagone

turtle.pendown() # La tortue va dessiner
turtle.forward(100) # Avance de 100px

turtle.left(72) # On tourne vers un angle de 72 degré vers la droite
turtle.forward(100) # Avance de 100px

turtle.left(72) # tourne à un angle de 72 degré à gauche
turtle.forward(100) # Avance de 100px

turtle.left(72) # tourne à un angle de 72 degré à gauche
turtle.forward(100) # Avance de 100px

turtle.right(72) # Tourne à un angle de 72 degré
turtle.forward(100) # Avance de 100px


turtle.done() # Affiche le résultat